#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t	index;

	if (dst == NULL || src == NULL)
		return ((size_t)0);
	index = 0;
	while (src[index] != '\0' && index + 1 < size)
	{
		dst[index] = src[index];
		index++;
	}
	if (size > 0)
		dst[index] = '\0';
	return (ft_strlen(src));
}
